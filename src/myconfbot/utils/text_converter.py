import re

def simple_text_to_html(text):
    """
    –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ—Ç –ø—Ä–æ—Å—Ç–æ–π —Ç–µ–∫—Å—Ç –≤ HTML —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ
    –¥–ª—è –æ–±—ã—á–Ω—ã—Ö –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π
    """
    # –ó–∞–≥–æ–ª–æ–≤–∫–∏ (—Å—Ç—Ä–æ–∫–∞ —Å ** –≤ –Ω–∞—á–∞–ª–µ –∏ –∫–æ–Ω—Ü–µ)
    text = re.sub(r'\*\*(.+?)\*\*', r'<b>\1</b>', text)
    
    # –ö—É—Ä—Å–∏–≤ (—Å—Ç—Ä–æ–∫–∞ —Å _ –≤ –Ω–∞—á–∞–ª–µ –∏ –∫–æ–Ω—Ü–µ)
    text = re.sub(r'_(.+?)_', r'<i>\1</i>', text)
    
    # –ú–æ–Ω–æ—à–∏—Ä–∏–Ω–Ω—ã–π (—Å—Ç—Ä–æ–∫–∞ —Å ` –≤ –Ω–∞—á–∞–ª–µ –∏ –∫–æ–Ω—Ü–µ)
    text = re.sub(r'`(.+?)`', r'<code>\1</code>', text)
    
    # –°–ø–∏—Å–∫–∏ (—Å—Ç—Ä–æ–∫–∏ –Ω–∞—á–∏–Ω–∞—é—â–∏–µ—Å—è —Å ‚Ä¢, -, –∏–ª–∏ *)
    text = re.sub(r'^[‚Ä¢\-*]\s+(.+)$', r'‚Ä¢ \1', text, flags=re.MULTILINE)
    
    # –ü–µ—Ä–µ–Ω–æ—Å—ã —Å—Ç—Ä–æ–∫
    text = text.replace('\n', '<br>')
    
    # –≠–∫—Ä–∞–Ω–∏—Ä–æ–≤–∞–Ω–∏–µ HTML —Å–∏–º–≤–æ–ª–æ–≤
    text = text.replace('&', '&amp;')
    text = text.replace('<', '&lt;')
    text = text.replace('>', '&gt;')
    
    return text

